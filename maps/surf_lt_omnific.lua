local function NewSpawn()

	for k, v in pairs(ents.FindByModel("models/props/surf_lt/tesseract.mdl")) do
		local e = ents.Create("surf_stopinitialmovement")
		e:SetTriggerPosition(v:LocalToWorld(v:OBBMins()), v:LocalToWorld(v:OBBMaxs()), v:GetPos())
		e:Spawn()
		v:Remove()
	end

	local playerSpawn = ents.Create("bb_player_spawn")
	playerSpawn:SetPos(Vector(6783.86328125, 11263.944335938, -767.96875))
	playerSpawn:SetAngles(Angle(0, 0, 0))
	playerSpawn:Spawn()
	hook.Run("SURF.UpdateSpawns")

	local blockZone = ents.Create("surf_blockzone")
	blockZone:SetTriggerPosition(Vector(1375.6009521484, -796.580078125, 3702.2312011719), Vector(760.60974121094, -1411.5704345703, 4508.7729492188))
	blockZone:SetPos(Vector(1026.5435791016, -1000.2744750977, 3976.03125))
	blockZone:Spawn()

	local noAhopAreas = {
		{Vector(7162.9194335938, 10240.48828125, -767.96875), Vector(7001.08984375, 12283.955078125, -0.03125)},
		{Vector(6589.1889648438, 11515.8359375, -767.96875), Vector(6932.1997070313, 11020.892578125, -0.03125)},
		{Vector(7059.4721679688, 10241.385742188, -767.96875),Vector(7004.8486328125, 12287.96875, -3.644775390625)}
	}

	for k, v in ipairs(noAhopAreas) do
		local noAhopZone = ents.Create("surf_disableahop")
		noAhopZone:SetTriggerPosition(v[1], v[2])
		noAhopZone:SetPos(v[1])
		noAhopZone:Spawn()
	end
end
hook.Add("InitPostEntity", "CreateNewSpawn", NewSpawn)

