local function NewSpawn()
	local playerSpawn = ents.Create("bb_player_spawn")
	playerSpawn:SetPos(Vector(-15585.329101563, 15132.1953125, 960))
	playerSpawn:SetAngles(Angle(0, 0, 0))
	playerSpawn:Spawn()
	hook.Run("SURF.UpdateSpawns")

	for k, v in ipairs(bonusAhopZones) do
		local bonus = ents.Create("surf_globalahop")
		bonus:SetTriggerPosition(v[1], v[2])
		bonus:Spawn()
	end
	
	for k, v in ipairs(noAhopZones) do
		local e = ents.Create("surf_disableahop")
		e:SetTriggerPosition(v[1], v[2])
		e:Spawn()
	end

	SURF.CreateAutoHopZones()

end
hook.Add("InitPostEntity", "CreateNewSpawn", NewSpawn)

