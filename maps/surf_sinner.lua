local function NewSpawn()
	local playerSpawn = ents.Create("bb_player_spawn")
	playerSpawn:SetPos(Vector(-907.07110595703, 5374.9887695313, 4159.03125))
	playerSpawn:SetAngles(Angle(0, 90, 0))
	playerSpawn:Spawn()
	hook.Run("SURF.UpdateSpawns")

	local stopTimerZone = ents.Create("surf_stoptimer_invis")
	stopTimerZone:SetTriggerPosition(Vector(1151.96875, 1932.162109375, 4403.3608398438), Vector(-1350.6462402344, -3469.6516113281, 3618.0998535156))
	stopTimerZone:Spawn()

	for k, v in ipairs(ents.FindByName("hub")) do
		v:SetPos(Vector(-907.07110595703, 5374.9887695313, 4159.03125))
		v:SetAngles(Angle(0, 90, 0))
	end

	RunConsoleCommand("sv_maxvelocity", 5000)
	SURF.CreateAutoHopZones()

	local bonus = ents.Create("surf_globalahop")
	bonus:SetTriggerPosition(Vector(6111.96875, 14350.979492188, -2607.9064941406), Vector(5088.03125, 13925.618164063, -2089.4377441406))
	bonus:Spawn()

